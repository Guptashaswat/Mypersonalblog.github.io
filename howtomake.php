The next time you and Pat plan to do something along these lines, pipe up and say, “Hey, Terry might like this too. Should we invite them?”


Or you can take the more direct approach and say to Pat, “Terry seems like someone I might have a lot in common with. Maybe the three of us should get together some time.”</p>
